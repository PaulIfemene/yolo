from PIL import Image

def convert_picture(input_path, output_path, output_format):
    try:
        img = Image.open(input_path)
        img.save(f"{output_path}.{output_format}")
        print(f"Image converted to {output_format}")
    except Exception as e:
        print(f"Error converting picture: {e}")


