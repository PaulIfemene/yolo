# music_converter.py
from pydub import AudioSegment

def convert_music(input_path, output_path, output_format):
    try:
        audio = AudioSegment.from_file(input_path)
        audio.export(f"{output_path}.{output_format}", format=output_format)
        print(f"Music converted to {output_format}")
    except Exception as e:
        print(f"Error converting music: {e}")

